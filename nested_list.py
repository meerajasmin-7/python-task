# list

list = [10, 20, 30, "Python"]
print(list[1])
list.append("java")
print(list)

# nested list

matrix = [[1, 2], [3, 4], [5, 6]]
print(matrix[0])      # [1, 2]
print(matrix[1][1])   # 4


# tuple

tuple = (10, 20, 30)
print(tuple[1])

# set

numbers = {1, 2, 2, 3, 4}
print(numbers)

# Dictionary

student = {"name": "jasmin", "age": 20, "courese": "python"}
print(student["name"])   # jasmin
student["age"] = 20
print(student,student["courese"])